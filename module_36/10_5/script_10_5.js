// Кредитный калькулятор
// Программа определяет максимальную сумму кредита в зависимости от возраста

function calculateMaxLoan(age) {
    if (age < 18) {
        return 0; // Кредит не выдается
    } else if (age >= 18 && age <= 21) {
        return 50000; // Максимум 50000
    } else if (age >= 22 && age <= 35) {
        return 400000; // Максимум 400000
    } else if (age >= 36 && age <= 65) {
        return 1000000; // Максимум 1000000
    } else {
        return 0; // Для лиц старше 65 лет кредит не выдается
    }
}

function getMaxLoanAmount() {
    // Запрашиваем возраст у пользователя
    const ageInput = prompt("Введите ваш возраст:");
    
    // Проверяем, что введено значение
    if (ageInput === null || ageInput === "") {
        alert("Возраст не был введен!");
        return;
    }
    
    // Преобразуем в число и проверяем, что это действительно число
    const age = Number(ageInput);
    
    if (isNaN(age) || age <= 0 || age > 120) {
        alert("Пожалуйста, введите корректный возраст (число от 1 до 120)!");
        return;
    }
    
    // Получаем максимальную сумму кредита
    const maxLoan = calculateMaxLoan(age);
    
    if (maxLoan === 0) {
        alert(`В возрасте ${age} лет кредит не выдается.`);
    } else {
        alert(`В возрасте ${age} лет вы можете получить кредит на сумму до ${maxLoan.toLocaleString()} рублей.\n\nСумма кредита должна быть кратна 1000 рублям.`);
    }
}

// Запускаем программу при загрузке страницы
getMaxLoanAmount();
